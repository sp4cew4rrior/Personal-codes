a = input('Please type something: ')
print('Here are some information about what you typed. ')
print('It primitive type is', type(a))
print('Its just spaces?', a.isspace())
print('It is alphanumeric?', a.isalnum())
print('It is alphabetic?', a.isalpha())
print('It is numeric?', a.isnumeric())
print('It is in capital letters?', a.isupper())
print('Is it in lower case?', a.islower())
print('Is it decimal?', a.isdecimal())
print('Is everything digits?', a.isdigit())


